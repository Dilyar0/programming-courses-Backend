#                                                       Базовые операции

#-----------------------------------------------Доступ по индексу
s3 = 'spameggs'

print(s3[3:5])  #'me'
print(s3[2:-2]) #'ameg'
print(s3[:6])   #'spameg'
print(s3[1:])   #'pameggs'
print(s3[:])    #'spameggs'

#--------------------------------------можно задать шаг, с которым нужно извлекать срез

print(s3[::-1])    #'sggemaps'
print(s3[3:5:-1])  #''
print(s3[2::2])     #'aeg'
